fn playbook_engine_page(snapshot: &JetpackSnapshot) -> String {
    let playbook_related: Vec<String> = snapshot
        .top_level_modules
        .iter()
        .filter(|name| {
            name.as_str() == "playbooks" || name.as_str() == "tasks" || name.as_str() == "inventory"
        })
        .cloned()
        .collect();

    format!(
        "{fm}Jetpack's execution path revolves around inventory, playbooks, and task execution.\n\n## Relevant Exported Modules\n\n{modules}\n",
        fm = frontmatter("Playbook Engine", 1),
        modules = bullet_list(&playbook_related)
    )
}

