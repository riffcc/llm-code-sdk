fn repository_data_page(snapshot: &JetpackSnapshot) -> String {
    let json_preview = escape_html(&serde_json::to_string_pretty(snapshot).unwrap_or_default());
    format!(
        "{fm}This page mirrors the structured repository snapshot that LCS exported into Hugo data.\n\n```json\n{preview}\n```\n",
        fm = frontmatter("Repository Data", 1),
        preview = json_preview
    )
}

fn capture(source: &Path) -> Result<JetpackSnapshot> {
    let cargo_toml = read_text(&source.join("Cargo.toml"))?;
    let readme = read_text(&source.join("README.md"))?;
    let lib_rs = read_text(&source.join("src/lib.rs"))?;

    Ok(JetpackSnapshot {
        package: parse_package_fields(&cargo_toml),
        top_level_modules: top_level_modules(&lib_rs),
        module_families: directory_names(&source.join("src/modules"))?,
        connection_backends: list_files(&source.join("src/connection"), 1)?,
        provisioners: list_files(&source.join("src/provisioners"), 1)?,
        docs_files: list_files(&source.join("docs"), 2)?,
        example_files: {
            let mut files = Vec::new();
            files.extend(list_files(&source.join("examples"), 2)?);
            files.extend(list_files(&source.join("jetpack_examples"), 2)?);
            files.extend(list_files(&source.join("crate-examples"), 2)?);
            files
        },
        readme_sections: parse_readme_sections(&readme),
    })
}

pub fn generate_jetpack_site(source: &Path, output: &Path) -> Result<()> {
    let snapshot = capture(source)?;

    ensure_site_dirs(output)?;
    ensure_hextra_theme(output)?;

    write_text(output, "hugo.yaml", JETPACK_HUGO_CONFIG)?;
    write_text(
        output,
        "layouts/_partials/scripts/search.html",
        "{{/* Search disabled for offline local builds. */}}\n",
    )?;
    write_text(output, "content/_index.md", &overview_page(&snapshot))?;
    write_text(
        output,
        "content/docs/_index.md",
        "---\ntitle: Docs\ncascade:\n  type: docs\n---\n\nJetpack documentation generated through LCS.\n",
    )?;
    write_text(output, "content/docs/introduction/_index.md", INTRO_INDEX)?;
    write_text(output, "content/docs/architecture/_index.md", ARCH_INDEX)?;
    write_text(output, "content/docs/usage/_index.md", USAGE_INDEX)?;
    write_text(output, "content/docs/reference/_index.md", REF_INDEX)?;
    write_text(
        output,
        "content/docs/introduction/repository-map.md",
        &repo_map_page(&snapshot),
    )?;
    write_text(
        output,
        "content/docs/architecture/module-families.md",
        &module_families_page(&snapshot),
    )?;
    write_text(
        output,
        "content/docs/architecture/playbook-engine.md",
        &playbook_engine_page(&snapshot),
    )?;
    write_text(
        output,
        "content/docs/architecture/connections.md",
        &connections_page(&snapshot),
    )?;
    write_text(
        output,
        "content/docs/architecture/provisioning.md",
        &provisioning_page(&snapshot),
    )?;
    write_text(
        output,
        "content/docs/usage/examples.md",
        &examples_page(&snapshot),
    )?;
    write_text(
        output,
        "content/docs/usage/debt-and-roadmap.md",
        &docs_and_debt_page(&snapshot),
    )?;
    write_text(
        output,
        "content/docs/reference/repository-data.md",
        &repository_data_page(&snapshot),
    )?;
    write_json(output, "data/jetpack.json", &json!(snapshot))?;

    Ok(())
}

#[cfg(test)]
mod tests {
    use super::{parse_package_fields, parse_readme_sections, top_level_modules};

    #[test]
    fn package_fields_are_extracted() {
        let cargo = "[package]\nname = \"jetpack\"\nversion = \"0.1.2\"\n";
        let fields = parse_package_fields(cargo);
        assert_eq!(fields.get("name").unwrap(), "jetpack");
        assert_eq!(fields.get("version").unwrap(), "0.1.2");
    }

    #[test]
    fn readme_sections_split_on_headings() {
        let readme = "Intro\n## First\nAlpha\n## Second\nBeta";
        let sections = parse_readme_sections(readme);
        assert!(sections.contains_key("Overview"));
        assert_eq!(sections.get("First").unwrap(), "Alpha");
        assert_eq!(sections.get("Second").unwrap(), "Beta");
    }

    #[test]
    fn top_level_modules_follow_pub_mod_lines() {
        let lib_rs = "pub mod cli;\npub mod tools;\n";
        assert_eq!(top_level_modules(lib_rs), vec!["cli", "tools"]);
    }
}
