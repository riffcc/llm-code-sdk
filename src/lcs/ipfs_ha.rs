fn theorem_index_page(snapshot: &IpfsHaSnapshot) -> String {
    let mut out = frontmatter("Theorems", 2);
    out.push_str(
        "Generated theorem cards for the current `FlatfsMw.lean` proof surface.\n\n",
    );
    for theorem in &snapshot.theorem_cards {
        out.push_str(&format!(
            "## [{}](/docs/reference/theorems/{})\n\n",
            theorem.short_name,
            slugify(&theorem.short_name)
        ));
        if let Some(doc) = &theorem.doc_comment {
            out.push_str(&format!("{}\n\n", doc));
        }
        out.push_str(&format!("Signature: `{}`\n\n", theorem.signature));
        let deps = if theorem.local_deps.is_empty() {
            "none".to_string()
        } else {
            theorem.local_deps
                .iter()
                .map(|dep| format!("`{dep}`"))
                .collect::<Vec<_>>()
                .join(", ")
        };
        out.push_str(&format!("Local deps: {}\n\n", deps));
    }
    out
}

fn theorem_page(theorem: &IpfsHaTheorem) -> String {
    let mut out = frontmatter(&theorem.short_name, 1);
    out.push_str(&format!(
        "Source: `proofs/FlatfsMw.lean` `{}`\n\n",
        theorem.line_anchor
    ));
    if let Some(doc) = &theorem.doc_comment {
        out.push_str(&format!("{}\n\n", doc));
    }
    out.push_str(&format!("## Signature\n\n`{}`\n\n", theorem.signature));
    out.push_str("## Local Dependencies\n\n");
    if theorem.local_deps.is_empty() {
        out.push_str("- none\n");
    } else {
        for dep in &theorem.local_deps {
            out.push_str(&format!("- `{}`\n", dep));
        }
    }
    out.push_str("\n## External / Unresolved References\n\n");
    if theorem.unresolved_refs.is_empty() {
        out.push_str("- none\n");
    } else {
        for dep in &theorem.unresolved_refs {
            out.push_str(&format!("- `{}`\n", dep));
        }
    }
    out.push_str("\n## Excerpt\n\n```lean\n");
    out.push_str(&theorem.excerpt);
    out.push_str("\n```\n");
    out
}

