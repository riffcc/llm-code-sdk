fn read_text(path: &Path) -> Result<String> {
    fs::read_to_string(path).with_context(|| format!("failed to read {}", path.display()))
}

fn relative(root: &Path, path: &Path) -> String {
    path.strip_prefix(root)
        .unwrap_or(path)
        .display()
        .to_string()
}

fn find_line(content: &str, needle: &str) -> Result<usize> {
    content
        .lines()
        .position(|line| line.contains(needle))
        .map(|idx| idx + 1)
        .ok_or_else(|| anyhow!("failed to find pattern `{needle}`"))
}

fn excerpt(content: &str, line: usize, radius: usize) -> String {
    let lines: Vec<&str> = content.lines().collect();
    if lines.is_empty() {
        return String::new();
    }

    let start = line.saturating_sub(radius + 1);
    let end = usize::min(lines.len(), line + radius);
    lines[start..end].join("\n")
}

fn proof_theorems(source_root: &Path) -> Result<Vec<String>> {
    let proof_path = source_root.join("proofs/FlatfsMw.lean");
    let source = read_text(&proof_path)?;

    #[cfg(feature = "smart")]
    {
        let mut parser = AstParser::new();
        let graph =
            LeanGraphAnalyzer::analyze(&proof_path.display().to_string(), &source, &mut parser)
                .ok_or_else(|| {
                    anyhow!("failed to build Lean graph for {}", proof_path.display())
                })?;

        return Ok(graph
            .declarations
            .into_iter()
            .filter(|decl| matches!(decl.kind, LeanDeclKind::Theorem))
            .map(|decl| decl.short_name)
            .collect());
    }

    #[cfg(not(feature = "smart"))]
    {
        let mut theorems = Vec::new();
        for line in source.lines() {
            let trimmed = line.trim();
            if let Some(rest) = trimmed.strip_prefix("theorem ") {
                if let Some(name) = rest.split_whitespace().next() {
                    theorems.push(name.trim_end_matches(':').to_string());
                }
            }
        }
        return Ok(theorems);
    }
}

fn proof_surface(source_root: &Path) -> Result<Vec<IpfsHaTheorem>> {
    let proof_path = source_root.join("proofs/FlatfsMw.lean");
    let source = read_text(&proof_path)?;

    #[cfg(feature = "smart")]
    {
        let mut parser = AstParser::new();
        let graph =
            LeanGraphAnalyzer::analyze(&proof_path.display().to_string(), &source, &mut parser)
                .ok_or_else(|| {
                    anyhow!("failed to build Lean graph for {}", proof_path.display())
                })?;

        return Ok(graph
            .declarations
            .into_iter()
            .filter(|decl| matches!(decl.kind, LeanDeclKind::Theorem))
            .map(|decl| IpfsHaTheorem {
                name: decl.name,
                short_name: decl.short_name,
                line_anchor: format!("L{}", decl.start_line),
                signature: decl.signature,
                doc_comment: decl.doc_comment,
                local_deps: decl.local_deps,
                unresolved_refs: decl.unresolved_refs,
                excerpt: excerpt(&source, decl.start_line, 6),
            })
            .collect());
    }

    #[cfg(not(feature = "smart"))]
    {
        let mut cards = Vec::new();
        for (idx, line) in source.lines().enumerate() {
            let trimmed = line.trim();
            if let Some(rest) = trimmed.strip_prefix("theorem ") {
                if let Some(name) = rest.split_whitespace().next() {
                    cards.push(IpfsHaTheorem {
                        name: name.trim_end_matches(':').to_string(),
                        short_name: name.trim_end_matches(':').to_string(),
                        line_anchor: format!("L{}", idx + 1),
                        signature: trimmed.to_string(),
                        doc_comment: None,
                        local_deps: Vec::new(),
                        unresolved_refs: Vec::new(),
                        excerpt: excerpt(&source, idx + 1, 6),
                    });
                }
            }
        }
        return Ok(cards);
    }
}

fn slugify(name: &str) -> String {
    let mut slug = String::new();
    let mut last_was_dash = false;
    for ch in name.chars() {
        if ch.is_ascii_alphanumeric() {
            slug.push(ch.to_ascii_lowercase());
            last_was_dash = false;
        } else if !last_was_dash {
            slug.push('-');
            last_was_dash = true;
        }
    }
    slug.trim_matches('-').to_string()
}

pub fn default_ipfs_ha_snapshot_path() -> PathBuf {
    PathBuf::from(DEFAULT_IPFS_HA_SNAPSHOT_PATH)
}

pub fn load_ipfs_ha_snapshot(path: &Path) -> Result<IpfsHaSnapshot> {
    let content = read_text(path)?;
    serde_json::from_str(&content)
        .with_context(|| format!("failed to parse IPFS HA snapshot {}", path.display()))
}

pub fn discover_ipfs_ha_snapshot(project_root: &Path) -> Option<PathBuf> {
    let explicit = default_ipfs_ha_snapshot_path();
    if explicit.exists() {
        let looks_like_ipfs_repo = project_root.join("proofs/FlatfsMw.lean").exists()
            || project_root.join("go-ds-flatfs-mw").exists()
            || project_root.join("kubo-v0.40.1").exists()
            || project_root
                .file_name()
                .and_then(|s| s.to_str())
                .map(|s| s.contains("IPFS-HA") || s.contains("ipfs"))
                .unwrap_or(false);
        if looks_like_ipfs_repo {
            return Some(explicit);
        }
    }

    let sibling = project_root.join("data/ipfs_ha_add_path.json");
    if sibling.exists() {
        return Some(sibling);
    }

    None
}

pub fn query_ipfs_ha_snapshot(snapshot: &IpfsHaSnapshot, query: &str) -> Vec<String> {
    let query_lc = query.to_ascii_lowercase();
    let mut blocks = Vec::new();

    for step in &snapshot.steps {
        let haystack = format!(
            "{} {} {} {} {} {}",
            step.route,
            step.component,
            step.file,
            step.line_anchor,
            step.ha_delta,
            step.excerpt
        )
        .to_ascii_lowercase();

        if haystack.contains(&query_lc)
            || query_lc.contains("ipfs")
            || query_lc.contains("kubo")
            || query_lc.contains("cluster")
            || query_lc.contains("flatfs")
            || query_lc.contains("moosefs")
            || query_lc.contains("proof")
        {
            blocks.push(format!(
                "IPFS-HA step: `{component}` at `{file}:{line}`\nRoute: `{route}`\nDelta: {delta}\n\n```\n{excerpt}\n```",
                component = step.component,
                file = step.file,
                line = step.line_anchor,
                route = step.route,
                delta = step.ha_delta,
                excerpt = step.excerpt
            ));
        }
    }

    for theorem in &snapshot.theorem_cards {
        let haystack = format!(
            "{} {} {} {} {} {}",
            theorem.name,
            theorem.short_name,
            theorem.signature,
            theorem.excerpt,
            theorem.local_deps.join(" "),
            theorem.unresolved_refs.join(" ")
        )
        .to_ascii_lowercase();

        if haystack.contains(&query_lc)
            || query_lc.contains("lean")
            || query_lc.contains("proof")
            || query_lc.contains("theorem")
        {
            blocks.push(format!(
                "IPFS-HA theorem: `{name}` at `proofs/FlatfsMw.lean:{line}`\nSignature: `{sig}`\nLocal deps: `{deps}`\nUnresolved refs: `{unresolved}`\n\n```\n{excerpt}\n```",
                name = theorem.short_name,
                line = theorem.line_anchor,
                sig = theorem.signature,
                deps = if theorem.local_deps.is_empty() {
                    "(none)".to_string()
                } else {
                    theorem.local_deps.join(", ")
                },
                unresolved = if theorem.unresolved_refs.is_empty() {
                    "(none)".to_string()
                } else {
                    theorem.unresolved_refs.join(", ")
                },
                excerpt = theorem.excerpt
            ));
        }
    }

    blocks.truncate(8);
    blocks
}

fn build_step(
    source_root: &Path,
    route: &str,
    component: &str,
    relative_path: &str,
    needle: &str,
    ha_delta: &str,
    proof_refs: &[String],
) -> Result<IpfsHaStep> {
    let path = source_root.join(relative_path);
    let content = read_text(&path)?;
    let line = find_line(&content, needle)?;

    Ok(IpfsHaStep {
        route: route.to_string(),
        component: component.to_string(),
        file: relative(source_root, &path),
        line_anchor: format!("L{}", line),
        ha_delta: ha_delta.to_string(),
        proof_refs: proof_refs.to_vec(),
        excerpt: excerpt(&content, line, 6),
    })
}

fn add_path_page(snapshot: &IpfsHaSnapshot) -> String {
    let mut out = frontmatter("IPFS HA Add Path", 1);
    out.push_str(
        "This page stitches the current add path across the deployed facade, Cluster, Kubo, shared-repo deltas, flatfs-mw, and the Lean proof surface.\n\n",
    );

    out.push_str("## Route Split\n\n");
    out.push_str("- `https://.../api/v0/add` -> Cluster facade\n");
    out.push_str("- `https://.../kubo/api/v0/add` -> direct Kubo add path\n");
    out.push_str("- Both routes force `chunker=size-1048576`\n\n");

    let mut grouped = std::collections::BTreeMap::<String, Vec<&IpfsHaStep>>::new();
    for step in &snapshot.steps {
        grouped.entry(step.route.clone()).or_default().push(step);
    }

    for (route, steps) in grouped {
        out.push_str(&format!("## `{}`\n\n", route));
        for step in steps {
            out.push_str(&format!(
                "### {}\n\nFile: `{}` `{}`\n\n{}\n\n",
                step.component, step.file, step.line_anchor, step.ha_delta
            ));

            if !step.proof_refs.is_empty() {
                out.push_str("Proof refs: ");
                out.push_str(
                    &step
                        .proof_refs
                        .iter()
                        .map(|name| format!("`{}`", name))
                        .collect::<Vec<_>>()
                        .join(", "),
                );
                out.push_str("\n\n");
            }

            let lang = if step.file.ends_with(".go") {
                "go"
            } else if step.file.ends_with(".lean") {
                "lean"
            } else {
                "text"
            };
            out.push_str(&format!("```{lang}\n{}\n```\n\n", step.excerpt));
        }
    }

    out
}

